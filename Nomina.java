    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int numerouno =0;
        int numerodos =0;
        int numerotres =0;
        int numerocuatro =0;
        
        numerouno = Integer.parseInt(this.numero1.getText());
        numerodos = Integer.parseInt(this.numero2.getText());
        numerotres = Integer.parseInt(this.numero3.getText());
        numerocuatro = Integer.parseInt(this.numero4.getText());
        
        int sal = numerouno + numerodos - numerotres - numerocuatro;
        
        salario.setText(String.valueOf(sal));
        
    }//GEN-LAST:event_jButton1ActionPerformed

